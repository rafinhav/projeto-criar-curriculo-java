// Classe Pessoa representa informações básicas que uma pessoa precisa no curriculo
public class Pessoa {
    private String nome;
    private String endereco;
    private String email;
    private String telefone;

    // Construtor da classe Pessoa
    public Pessoa(String nome, String endereco, String email, String telefone) {
        this.nome = nome;
        this.endereco = endereco;
        this.email = email;
        this.telefone = telefone;
    
    }
    
    
    /**
     * @return the nome
     */
    
    // Métodos getters para obter os atributos da pessoa
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    
    //Método setters para colocar a informação dos atributos
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the endereco
     */
    public String getEndereco() {
        return endereco;
    }

    /**
     * @param endereco the endereco to set
     */
    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the telefone
     */
    public String getTelefone() {
        return telefone;
    }

    /**
     * @param telefone the telefone to set
     */
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

}